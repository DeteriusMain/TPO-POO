public class EstadoPedido {
    // Clase de EstadoPedido
    public boolean aprobado;

    // Constructor
    public EstadoPedido(boolean aprobado) {
        this.aprobado = aprobado;
    }

    // Getter and Setter
    public boolean isAprobado() {
        return aprobado;
    }

    public void setAprobado(boolean aprobado) {
        this.aprobado = aprobado;
    }
}